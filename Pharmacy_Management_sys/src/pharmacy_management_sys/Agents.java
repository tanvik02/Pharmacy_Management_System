void SetDataU(String x){
    try{
        OpenConnection();
        int x1=Integer.parseInt(x);
        String q="Select * from agenttb where AId="+x1;
        rs=pst.executeQuery(q);
        if(rs.next()){
            txt_aid.setText(x);
            String name=rs.getString("AName");
            txt_aname.setText(name);
            int age = rs.getInt("AAge");
            txt_aage.setText(String.valueOf(age));
            String ph=rs.getString("APh");
            txt_aph.setText(ph);
            String pass=rs.getString("APass");
            txt_aph.setText(pass);
            String gen=rs.getString("AGen");
            cbG.setSelectedItem(gen);
            

        }
    }
    catch(Exception e)
            {
                  JOptionPane.showMessageDialog(null,e.getMessage());  
             }
}

