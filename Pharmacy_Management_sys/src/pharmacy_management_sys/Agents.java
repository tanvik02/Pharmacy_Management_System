    public void updateDB(){
        try
        {
            OpenConnection();
            pst= sqlConn.prepareStatement("select * from agenttb;");
            
            rs = pst.executeQuery();
            ResultSetMetaData stData= rs.getMetaData();
            q= stData.getColumnCount();
            
            DefaultTableModel RecordTable = (DefaultTableModel)tbAgent.getModel();
            RecordTable.setRowCount(0);
            
            while(rs.next()){
                Vector columnData= new Vector();
                
                for(i=1;i<=q;i++){
                    columnData.add(rs.getInt("AId"));
                    columnData.add(rs.getString("AName"));
                    columnData.add(rs.getInt("AAge"));
                    columnData.add(rs.getString("APh"));
                    columnData.add(rs.getString("APass"));
                    columnData.add(rs.getString("AGen"));
                    
                    
                }
                RecordTable.addRow(columnData);
            }
            
        }
        catch (Exception ex){
            JOptionPane.showMessageDialog(null,ex);
        }
    }
    
